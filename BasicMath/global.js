var numOne = Number(prompt("Please enter a number"));
var numTwo = Number(prompt("Please enter a second number"));
var numThree = Number(prompt("Please enter a third number"));

alert(numOne + numTwo + numThree);
alert(numOne - numTwo - numThree);
alert(numOne * numTwo * numThree);
alert(numOne / numTwo);
alert(numOne++);
alert(numTwo--);